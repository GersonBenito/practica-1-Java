package Constantes;

import java.util.Date;

public class Constantes {
    public static void main(String[] args){
        // Constantes
        // para declarar una variable constante se debe de usar la palabra reservadao final
        final double PI = 3.1416;
        final String nombe = "Gerson Benito";
        final int modulos = 100;

        System.out.println("Numero PI: " + PI);
        System.out.println("Nombre: " + nombe);
        System.out.println("Modulos: " + modulos);
    }
}
